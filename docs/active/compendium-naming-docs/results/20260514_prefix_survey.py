"""Survey row_id prefixes across the 181-row v2 compendium.

Produces:
- Prefix-family histogram (greedy 1-token, 2-token, 3-token prefixes)
- Per-family member listing
- Row-id length distribution
- Cross-tabs of prefix family × cell_type and × axis

Output written alongside this script as 20260514_prefix_survey.md.
"""
from __future__ import annotations

import csv
import sys
from collections import Counter, defaultdict
from pathlib import Path

TSV = Path(__file__).resolve().parents[4] / "compendium" / "disclosure_side_compendium_items_v2.tsv"
OUT = Path(__file__).with_suffix(".md")


def main() -> int:
    rows: list[dict[str, str]] = []
    with TSV.open() as fh:
        reader = csv.DictReader(fh, delimiter="\t")
        for r in reader:
            rows.append(r)

    n = len(rows)

    # 1-token, 2-token, 3-token prefix histograms
    def prefix(row_id: str, k: int) -> str:
        toks = row_id.split("_")
        return "_".join(toks[:k]) if len(toks) >= k else row_id

    h1 = Counter(prefix(r["compendium_row_id"], 1) for r in rows)
    h2 = Counter(prefix(r["compendium_row_id"], 2) for r in rows)
    h3 = Counter(prefix(r["compendium_row_id"], 3) for r in rows)

    # Family membership at the 2-token level (most useful granularity from
    # eyeballing the issue: def_target_*, def_actor_class_*, actor_*,
    # lobbyist_reg_form_*, lobbyist_spending_report_*, etc.)
    by_2tok: dict[str, list[str]] = defaultdict(list)
    for r in rows:
        by_2tok[prefix(r["compendium_row_id"], 2)].append(r["compendium_row_id"])

    # Length distribution
    lengths = Counter(len(r["compendium_row_id"].split("_")) for r in rows)

    # Cross-tab: 1-tok prefix × cell_type
    xt_cell: dict[str, Counter[str]] = defaultdict(Counter)
    for r in rows:
        xt_cell[prefix(r["compendium_row_id"], 1)][r["cell_type"]] += 1

    # Cross-tab: 1-tok prefix × axis
    xt_axis: dict[str, Counter[str]] = defaultdict(Counter)
    for r in rows:
        xt_axis[prefix(r["compendium_row_id"], 1)][r["axis"]] += 1

    # Cross-tab: 1-tok prefix × first_introduced_by (rubric provenance)
    xt_first: dict[str, Counter[str]] = defaultdict(Counter)
    for r in rows:
        xt_first[prefix(r["compendium_row_id"], 1)][r["first_introduced_by"]] += 1

    # Write markdown
    lines: list[str] = []
    lines.append("# Prefix survey — v2 compendium row IDs")
    lines.append("")
    lines.append(f"Source: `{TSV.relative_to(TSV.parents[2])}` ({n} rows)")
    lines.append("")
    lines.append("Generated by `20260514_prefix_survey.py` on 2026-05-14.")
    lines.append("")

    lines.append("## Row-ID length distribution (tokens)")
    lines.append("")
    lines.append("| Tokens | Count |")
    lines.append("|---:|---:|")
    for k in sorted(lengths):
        lines.append(f"| {k} | {lengths[k]} |")
    lines.append("")

    lines.append("## 1-token prefix histogram")
    lines.append("")
    lines.append("| Prefix | Count |")
    lines.append("|---|---:|")
    for p, c in h1.most_common():
        lines.append(f"| `{p}_*` | {c} |")
    lines.append("")

    lines.append("## 2-token prefix histogram (count >= 2 only)")
    lines.append("")
    lines.append("| Prefix | Count |")
    lines.append("|---|---:|")
    for p, c in h2.most_common():
        if c >= 2:
            lines.append(f"| `{p}_*` | {c} |")
    lines.append("")
    n_singletons_2 = sum(1 for c in h2.values() if c == 1)
    lines.append(f"_{n_singletons_2} singleton 2-token prefixes omitted._")
    lines.append("")

    lines.append("## 3-token prefix histogram (count >= 3 only)")
    lines.append("")
    lines.append("| Prefix | Count |")
    lines.append("|---|---:|")
    for p, c in h3.most_common():
        if c >= 3:
            lines.append(f"| `{p}_*` | {c} |")
    lines.append("")

    lines.append("## 1-token prefix × cell_type")
    lines.append("")
    cell_types = sorted({ct for r in rows for ct in [r["cell_type"]]})
    header = "| Prefix | " + " | ".join(cell_types) + " |"
    sep = "|---|" + "|".join("---:" for _ in cell_types) + "|"
    lines.append(header)
    lines.append(sep)
    for p in sorted(xt_cell, key=lambda k: -sum(xt_cell[k].values())):
        cells = [str(xt_cell[p].get(ct, "")) for ct in cell_types]
        lines.append(f"| `{p}_*` | " + " | ".join(cells) + " |")
    lines.append("")

    lines.append("## 1-token prefix × axis")
    lines.append("")
    axes = sorted({r["axis"] for r in rows})
    header = "| Prefix | " + " | ".join(axes) + " |"
    sep = "|---|" + "|".join("---:" for _ in axes) + "|"
    lines.append(header)
    lines.append(sep)
    for p in sorted(xt_axis, key=lambda k: -sum(xt_axis[k].values())):
        cells = [str(xt_axis[p].get(a, "")) for a in axes]
        lines.append(f"| `{p}_*` | " + " | ".join(cells) + " |")
    lines.append("")

    lines.append("## 1-token prefix × first_introduced_by")
    lines.append("")
    firsts = sorted({r["first_introduced_by"] for r in rows})
    header = "| Prefix | " + " | ".join(firsts) + " |"
    sep = "|---|" + "|".join("---:" for _ in firsts) + "|"
    lines.append(header)
    lines.append(sep)
    for p in sorted(xt_first, key=lambda k: -sum(xt_first[k].values())):
        cells = [str(xt_first[p].get(f, "")) for f in firsts]
        lines.append(f"| `{p}_*` | " + " | ".join(cells) + " |")
    lines.append("")

    lines.append("## 2-token family membership")
    lines.append("")
    lines.append("Full membership listing per 2-token prefix family (count >= 2):")
    lines.append("")
    for p, members in sorted(by_2tok.items(), key=lambda kv: (-len(kv[1]), kv[0])):
        if len(members) < 2:
            continue
        lines.append(f"### `{p}_*` ({len(members)} rows)")
        lines.append("")
        for m in sorted(members):
            lines.append(f"- `{m}`")
        lines.append("")

    lines.append("## Singleton 2-token prefixes (count == 1)")
    lines.append("")
    singletons = sorted(m for p, members in by_2tok.items() if len(members) == 1 for m in members)
    lines.append(f"_{len(singletons)} rows whose 2-token prefix is unique._")
    lines.append("")
    for s in singletons:
        lines.append(f"- `{s}`")
    lines.append("")

    OUT.write_text("\n".join(lines))
    print(f"Wrote {OUT} ({len(lines)} lines)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
